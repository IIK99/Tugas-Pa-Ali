#Stacking and Queue

# Stacking adalah tumpukan data

Stacks = [1,2,3,4,5,6]
print('Stacks now: ',Stacks)

# Menambahkan tumpukan data baru
# Append menambahkan data
Stacks.append(7)
print('Data in: ',7)
print('Stacks now: ',Stacks)

Stacks.append(8)
print('Data in: ',8)
print('Stacks now: ',Stacks)

# Pop mengurangi data
Out = Stacks.pop()
print('Data out: ',Out)
print('Data now: ',Stacks)